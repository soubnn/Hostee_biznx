    {
        $data = $request->all();
        $data['date']       = Carbon::now();
        $data['add_by']     = Auth::user()->name;
        $status= Expense::create($data);

        if($status){

            $expense_details = DB::table('expenses')->latest('id')->first();
            return $expense_details;
        }
        else{

            $expense_details = "Error";
        }
        return response()->json($expense_details);
    }

